#!/bin/bash
# gen-results-table.sh - Generate LaTeX results table from bench_results.csv
#
# Reads the merged CSV (with fragment column) and outputs a LaTeX table
# suitable for inclusion in the paper.
#
# Usage:
#   ./scripts/gen-results-table.sh bench_results.csv > tex/results-table.tex

set -euo pipefail
export LC_NUMERIC=C

CSV="${1:-bench_results.csv}"

if [[ ! -f "$CSV" ]]; then
  echo "Error: CSV not found: $CSV" >&2
  exit 1
fi

# Verify it has the fragment column (7+ fields)
n_fields=$(head -1 "$CSV" | awk -F, '{print NF}')
if [[ "$n_fields" -lt 7 ]]; then
  echo "Error: CSV must have fragment column (7 fields). Re-run bench.sh." >&2
  exit 1
fi

awk -F, '
BEGIN {
  # Ordering: quantifier-free first, then quantified, alphabetical within
  split("QF_UF QF_AX QF_IDL QF_LIA QF_NIA QF_ALIA QF_UFIDL QF_UFLIA QF_UFNIA QF_AUFLIA QF_AUFNIA QF_ANIA UF UFIDL UFLIA UFNIA ALIA AUFLIA AUFNIA ANIA LIA NIA", order_arr)
  for (i in order_arr) order[order_arr[i]] = i
}
NR > 1 {
  frag = $1
  status = $3
  enc_time = $4 + 0
  chk_time = $5 + 0
  ecat = $6

  total[frag]++
  if (status == "pass") {
    pass[frag]++
    sum_enc[frag] += enc_time
    sum_chk[frag] += chk_time
  } else if (status == "fail_encode") {
    if (ecat == "timeout") timeout[frag]++
    else enc_fail[frag]++
  } else if (status == "fail_check") {
    if (ecat == "timeout") timeout[frag]++
    else chk_fail[frag]++
  }
}
END {
  # Collect fragments that have proofs
  n = 0
  for (f in total) {
    n++
    frags[n] = f
  }

  # Sort by our preferred order
  for (i = 1; i <= n; i++) {
    for (j = i+1; j <= n; j++) {
      oi = (frags[i] in order) ? order[frags[i]] : 999
      oj = (frags[j] in order) ? order[frags[j]] : 999
      if (oj < oi || (oi == oj && frags[j] < frags[i])) {
        tmp = frags[i]; frags[i] = frags[j]; frags[j] = tmp
      }
    }
  }

  # Escape underscores for LaTeX
  gsub_us = 1

  # Print table
  print "% Auto-generated by scripts/gen-results-table.sh"
  print "% Do not edit manually; re-generate with:"
  print "%   ./scripts/gen-results-table.sh bench_results.csv > tex/results-table.tex"
  print "\\begin{table}[t]"
  print "\\centering"
  print "\\caption{Benchmark results across SMT-LIB fragments.}"
  print "\\label{tab:results}"
  print "\\begin{tabular}{l r r r r r r r}"
  print "\\hline"
  print "Fragment & Total & Pass & \\multicolumn{1}{c}{\\%} & Enc. & Chk. & T/O & Avg. (s) \\\\"
  print "\\hline"

  g_total = 0; g_pass = 0; g_enc = 0; g_chk = 0; g_tmo = 0
  g_enc_time = 0; g_chk_time = 0

  for (i = 1; i <= n; i++) {
    f = frags[i]
    p  = pass[f] + 0
    ef = enc_fail[f] + 0
    cf = chk_fail[f] + 0
    to = timeout[f] + 0
    t  = total[f]

    g_total += t; g_pass += p; g_enc += ef; g_chk += cf; g_tmo += to
    g_enc_time += sum_enc[f]; g_chk_time += sum_chk[f]

    pct = (t > 0) ? sprintf("%.0f", 100.0 * p / t) : "0"

    # Average total time (encode + check) for passing proofs
    avg = ""
    if (p > 0) {
      a = (sum_enc[f] + sum_chk[f]) / p
      avg = sprintf("%.2f", a)
    } else {
      avg = "--"
    }

    # Escape underscores in fragment name
    fname = f
    gsub(/_/, "\\_", fname)

    # Use texttt for fragment names
    printf "\\texttt{%s} & %d & %d & %s & %d & %d & %d & %s \\\\\n",
      fname, t, p, pct, ef, cf, to, avg
  }

  print "\\hline"

  # Totals
  g_pct = (g_total > 0) ? sprintf("%.0f", 100.0 * g_pass / g_total) : "0"
  g_avg = "--"
  if (g_pass > 0) g_avg = sprintf("%.2f", (g_enc_time + g_chk_time) / g_pass)

  printf "\\textbf{Total} & \\textbf{%d} & \\textbf{%d} & \\textbf{%s} & \\textbf{%d} & \\textbf{%d} & \\textbf{%d} & \\textbf{%s} \\\\\n",
    g_total, g_pass, g_pct, g_enc, g_chk, g_tmo, g_avg

  print "\\hline"
  print "\\end{tabular}"
  print "\\end{table}"
}
' "$CSV"
